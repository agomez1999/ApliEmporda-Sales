<?php

namespace Daw;

class ModelPDO
{
    protected $sql;

    public function __construct($config)
    {
        $dsn = "mysql:dbname={$config['dbname']};host={$config['host']}";
        $usuari = $config['user'];
        $clau = $config['pass'];

        try {
            $this->sql = new \PDO($dsn, $usuari, $clau);
        } catch (\PDOException $e) {
            die('Ha fallat la connexiÃ³: ' . $e->getMessage() . " $usuari");
        }
    }
  
}
